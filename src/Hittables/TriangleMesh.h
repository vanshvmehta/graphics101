	static bool insideTriangle(std::vector<float> triXs, std::vector<float> triYs, float x, float y) {
		double dX1 = triXs[1] - triXs[0];
		double dX2 = triXs[2] - triXs[1];
		double dX3 = triXs[0] - triXs[2];

		double dY1 = triYs[1] - triYs[0];
		double dY2 = triYs[2] - triYs[1];
		double dY3 = triYs[0] - triYs[2];

		double l1 = -1 * (x - triXs[0])*dY1 + (y - triYs[0])*dX1;
		double l2 = -1 * (x - triXs[1])*dY2 + (y - triYs[1])*dX2;
		double l3 = -1 * (x - triXs[2])*dY3 + (y - triYs[2])*dX3;

		return (l1 >= 0 && l2 >= 0 && l3 >= 0);
	}

	void rasterizeTriangle(const Triangle& tri, const float4x4& plm) {
		// ====== implement it in A1 ======
		// 	// rasterization of a triangle
		// 	// "plm" should be a matrix that contains perspective projection and the camera matrix
		// 	// you do not need to implement clipping
		// 	// you may call the "shade" function to get the pixel value
		// 	// (you may ignore viewDir for now)
		// 	// Loop over each vertex of the triangle
	    std::vector<float> triXs(3);
	    std::vector<float> triYs(3);
		std::vector<float> triZs(3);
	    std::vector<float> triWs(3);
	    std::vector<float2> triUVs(3);

	    for (int v = 0; v < 3; ++v) {
	        float4 vertex = mul(plm, float4{tri.positions[v].x, tri.positions[v].y, tri.positions[v].z, 1.0f});
	        float w = vertex.w;

	        float x = vertex.x / w;
	        float y = vertex.y / w;
	        float z = vertex.z / w;

	        float i = (x * 0.5f + 0.5f) * FrameBuffer.width;
	        float j = (y * 0.5f + 0.5f) * FrameBuffer.height;

	        triXs[v] = i;
	        triYs[v] = j;
	    	triZs[v] = z;
	        triWs[v] = 1.0f / w;
	        triUVs[v] = {tri.texcoords[v].x / w, tri.texcoords[v].y / w};

	    	// FrameBuffer.pixel(i, j) = float3(1.0f);
	    }

	    int minX = *std::min_element(triXs.begin(), triXs.end());
	    int maxX = *std::max_element(triXs.begin(), triXs.end());
	    int minY = *std::min_element(triYs.begin(), triYs.end());
	    int maxY = *std::max_element(triYs.begin(), triYs.end());

	    minX = std::max(minX, 0);
	    maxX = std::min(maxX, FrameBuffer.width - 1);
	    minY = std::max(minY, 0);
	    maxY = std::min(maxY, FrameBuffer.height - 1);

	    for (int y = minY; y <= maxY; ++y) {
	        for (int x = minX; x <= maxX; ++x) {
	            if (insideTriangle(triXs, triYs, x + 0.5f, y + 0.5f)) {
	                float w1 = ((triYs[1] - triYs[2]) * (x - triXs[2]) + (triXs[2] - triXs[1]) * (y - triYs[2])) /
	                                ((triYs[1] - triYs[2]) * (triXs[0] - triXs[2]) + (triXs[2] - triXs[1]) * (triYs[0] - triYs[2]));
	                float w2 = ((triYs[2] - triYs[0]) * (x - triXs[2]) + (triXs[0] - triXs[2]) * (y - triYs[2])) /
	                                ((triYs[1] - triYs[2]) * (triXs[0] - triXs[2]) + (triXs[2] - triXs[1]) * (triYs[0] - triYs[2]));
	                float w3 = 1.0f - w1 - w2;

	                float w = w1 * triWs[0] + w2 * triWs[1] + w3 * triWs[2];
	                float u = w1 * triUVs[0].x + w2 * triUVs[1].x + w3 * triUVs[2].x;
	                float v = w1 * triUVs[0].y + w2 * triUVs[1].y + w3 * triUVs[2].y;
	                float z = w1 * triZs[0] + w2 * triZs[1] + w3 * triZs[2];

	                u /= w;
	                v /= w;
	                z /= w;

	                if (z < FrameBuffer.depth(x, y)) {
	                    FrameBuffer.depth(x, y) = z;

	                    HitInfo hit;
	                    hit.T = {u, v};
	                    hit.material = &materials[tri.idMaterial];

	                    float3 color = shade(hit, {0, 0, 0}, 0);
	                    FrameBuffer.pixel(x, y) = color;
	                }
	            }
	        }
	    }
	}

